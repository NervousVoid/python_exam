import math


def main(x, z, y):
    n = len(x)
    f = 0
    for i in range(1, n + 1):
        f += 39 * ((x[n + 1 - math.ceil(i / 4) - 1] / 64 -
                    32 * y[n + 1 - math.ceil(i / 2) - 1] ** 3 -
                    74 * z[n + 1 - i - 1] ** 2))
    return 44 * f

